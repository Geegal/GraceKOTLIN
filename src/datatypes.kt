fun main() {
    var firstnumber :Int = 20
    var mynumber :Double = 34.56
    var animal :String = "dog"
    var myletter:Char = 'C'
    var isKotlininteresting:Boolean = true

    println(firstnumber)
    println(mynumber)
    println(myletter)
    println(animal)
    println(isKotlininteresting)

